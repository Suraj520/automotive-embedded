% MATLAB Arithmetic Operations

% Define two numbers for the operations
a = 10;
b = 3;

% Addition
add_result = a + b;
disp(['Addition: ', num2str(add_result)]);

% Subtraction
sub_result = a - b;
disp(['Subtraction: ', num2str(sub_result)]);

% Multiplication
mul_result = a * b;
disp(['Multiplication: ', num2str(mul_result)]);

% Division
div_result = a / b;
disp(['Division: ', num2str(div_result)]);

% Exponentiation (a to the power of b)
exp_result = a ^ b;
disp(['Exponentiation: ', num2str(exp_result)]);

% Modulo (Remainder after division of a by b)
mod_result = mod(a, b);
disp(['Modulo: ', num2str(mod_result)]);

% Square root
sqrt_result = sqrt(a);
disp(['Square Root of a: ', num2str(sqrt_result)]);

% Absolute value
abs_result = abs(-a);
disp(['Absolute Value of -a: ', num2str(abs_result)]);
